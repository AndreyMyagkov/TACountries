        <p>Угрозы терроризма в Доминикане практически нет. Но опасность исходит от местных жителей, нередки волнения, которые перерастают в массовые беспорядки и стычки с полицией, особенно в преддверии выборов. Поэтому рекомендуем обходить стороной демонстрации и избегать походов в бедные районы страны.</p>

        <p>Вследствие того, что население в Доминикане живет бедно - здесь очень распространены преступления, среди которых уличные грабежи, карманные кражи, мошенничество с кредитными картами и угоны автомобилей.</p>
