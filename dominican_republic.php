        <p>Наибольшее респространение получили уличные грабежи, причем совершаются они и днем и ночью, группами и в одиночку, часто на мотоциклах и велосипедах. Так, грабители подъезжают к жертве, выхватывают сумки, фотоаппараты, мобильные телефоны и быстро уезжают. Раскрываемость таких преступлений почти нулевая, поэтому не носите ценные вещи в руках и карманах, и следите за проезжающими мимо мотоциклистами.</p>
