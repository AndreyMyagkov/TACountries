                <p>Доминиканцы обладают радушным, приветливым, оптимистичным и часто беззаботным нравом. Островное расположение и климатические условия способствуют размеренному, неторопливому и даже несколько ленивому образу жизни.</p>
                <p>Местные жители всей душой любят отмечать национальные праздники и участвовать в различных карнавалах.</p>
