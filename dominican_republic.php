                <p>Что касается национальной кухни, то кухня Доминиканы исторически соединила в себе африканские, индейские и испанские традиции. К примеру, здесь пользуются популярностью как испанская паэлья, так и лепешки из маниоки, рецепт которых не менялся со времен расселения индейских племен Таино.</p>
                <p>Основу блюд составляют рис, бобы, маниока, овощные бананы, свинина, курица, морепродукты и тропические фрукты. Мясо и морепродукты обычно тушат в кокосовом молоке или жарят на гриле. Типичным блюдом является «ла бандера» — это рагу из риса и мяса с добавлением фасоли и жареных бананов. Вообще, бананы используются очень широко как в десертах, так и в основных блюдах — их тушат, варят, жарят, сушат и добавляют в большинство блюд.</p>
