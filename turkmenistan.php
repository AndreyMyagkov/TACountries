                <h2>Культура и традиции</h2>

                <p>На культуру Туркменистана, ее самобытность и аутентичность, отпечаток накладывали религиозные догмы и племенные отношения, которые складывались столетиями.</p>
                <p>Туркмены — очень радушный и гостеприимный народ, отличающийся высокой нравственностью. Главными их ценностями являются: семья, почитание старших, честность, скромность, благородство, правдивость и щедрость.</p>
                <p>Многие местные жители по сей день носят традиционную национальную одежду: для женщин это длинные платья из шелка или хлопка, шаровары и косынка, а мужчины надевают свободные брюки, халаты и папахи.</p>
                <p>Приверженность традициям также проявляется во многих праздниках, как общенародных, так и семейных. К примеру, одним из важнейших событий жизни туркменов является празднование бракосочетания. За невесту ее семье издревле полагался выкуп — калым, этот обычай является актуальным и по сегодняшний день. Традиционно платье невесты украшалось замысловатой вышивкой и множеством ювелирных изделий, а также дополнялось различными причудливыми оберегами, например, клыками животных или косичками из шерсти верблюда. Основные гуляния проводились на стороне жениха и длились по нескольку дней.</p>
                <p>Также одним из главных направлений в культуре Туркмении и символом страны является ковроткачество. Ковер всегда был основным, а порой и единственным предметом мебели у кочевников — он легко перевозился, служил настилом и постелью и, конечно, являлся настоящим украшением жилья. Искусство коврового ткачества очень трудоемкое и сложное, а его техника передается от поколения поколению.</p>
                <p>Особой частью культуры любого народа является национальная кухня. Отличительной чертой местной кухни является простота приготовления блюд и питательность.</p>
                <p>Основу рациона составляет мясо: баранина, верблюжатина, курица, дичь. Также широко употребляются кисломолочные продукты, рис, масло из верблюжьего молока, кунжутное масло и изделия из муки.</p>
                <p>Гостям страны непременно стоит попробовать местные популярные блюда:</p>

                <ul>
