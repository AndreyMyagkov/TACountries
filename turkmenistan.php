                    <li>кокмач — отбивная баранина, подается с рисом или картофелем;</li>
                    <li>суп-шурпа — бульон из баранины, с луком, помидором и морковью;</li>
                    <li>чорба нохуды — гороховый суп из баранины;</li>
                    <li>сладкие пирожки с хурмой;</li>
                    <li>кульче — лепешки из кислого теста, выпекаемые в тандыре.</li>
