                <p>Польская культура тесно связана с российским и украинским искусством, и, конечно, сильнейшее влияние на ее формирование оказывала религия. Наиболее ярко это влияние наблюдается в интересных традициях, связанных с религиозным праздниками и народными гуляниями. Так, например, одним из главных праздников в стране является Рождество. В этот день устраиваются рождественские театрализованные шествия со звездой, во время которых дети и молодежь разыгрывают библейские сценки. Ряженые ходят от дома к дому, поют рождественские песни и получают за это сладости и монеты от хозяев. В сочельник щедро накрывают на стол (обязательно должно быть 12 блюд) и оставляют одно место свободным в память о покинувших мир близких людях и родственниках.</p>
                <p>Другим важным праздником в стране является Пасха, которая, как и Рождество, отмечается по католическому календарю. Подготовка к празднику ведется задолго: многие верующие соблюдают строгий пост, в домах проводится генеральная уборка. Накануне Пасхи хозяйки окрашивают яйца (писанки) и подготавливают праздничные корзины: дно застилают льняной салфеткой, украшают ветвям самшита, кладут писанки, ветчину, куличи, хлеб и символичного пасхального барашка. Эти угощения обязательно освящают в церкви. В день Пасхи принято посещать церковную службу, после чего вся семья собирается дома за праздничным обедом. В Польше по сей день существует любопытный народный обычай — в день Пасхи обливать водой прохожих, что ассоциируется с благословением и крещением.</p>
                <p>У поляков сохранилось множество трогательных традиций, касающихся главной ценности — семьи. Так, например, практически каждому члену семьи в году посвящается день (день матери, отца, бабушки, дедушки и т.д.).</p>
                <p>Неотъемлемой частью культуры страны является ее национальная кухня. Местные блюда отличаются питательностью, калорийностью и ярким вкусом. Излюбленными ингредиентами поляков являются мясо, различные сорта колбасы, квашеная капуста, грибы, сметана и яйца. Трапезу в Польше, как правило, начинают с супов. Самым необычным из них является «чернина» — суп из крови гуся с добавлением потрошков, сухофруктов и овощей.</p>
                <p>В качестве второго блюда популярны вареники с кислой капустой, грибами или картошкой, капустные голубцы в томатном соусе, картофельные оладьи и бигос — свинина или копченые колбаски с кислой капустой и черносливом.</p>
