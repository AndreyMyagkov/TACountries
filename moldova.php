                <h2>Культура и традиции</h2>
                <p>Культурное наследие Молдавии очень богато и отличается разнообразием традиций и обычаев, сохранившихся еще с древних времен гетто-даков — племен, населявших территорию нынешнего государства. Эти традиции связаны с народной музыкой, песнями и танцами, особенностями проведений праздников, национальной кухней, виноделием, изготовлением предметов для быта.</p>
                <p>Во многих селениях важнейшим ремеслом являлась керамика — мастера изготавливали искусно оформленные горшки, тарелки, кувшины. Также в сёлах имело большое распространение искусство ручной вышивки, отличающееся большой цветовой гаммой и сложностью узоров. Вышивками украшались одежда, постельные принадлежности, занавески, скатерти и ковры.</p>
                <p>Интересны традиции Молдовы, связанные с праздниками. Например, одним из главных и любимых событий в стране является Мэрцишор — он отмечается 1 марта, в день наступления долгожданной весны. В праздник принято поздравлять друг друга и дарить украшения из двух цветков. Этот символ жители носят весь месяц, а после — загадывают желания и подвешивают на ветви дерева.</p>
                <p>Еще одним оригинальным и привлекательным праздником для местных жителей и гостей страны является День Вина, отмечающийся в октябре. В этот день на главной площади столицы устраивается массовое веселье с национальными песнями и плясками. Здесь можно приобрести памятные сувениры, отведать блюда национальной кухни и, разумеется, продегустировать вино — символ Молдавии и виновник торжества.</p>
                <p>Национальная кухня — это гордость молдаван. В стране хорошо развито животноводство и птицеводство, а также имеются богатые природные возможности для выращивания овощей и фруктов. Поэтому молдавская кухня отличается разнообразием и яркостью вкуса.</p>
                <p>Наиболее популярным кушаньем является мамалыга — это каша со сметаной, шкварками, яйцами и брынзой. К другим известным блюдам относятся мусака (слоеная запеканка из баклажанов), гювеч (ароматное рагу с мясом и овощами), мититеи (маленькие колбаски на мангале) и зама (вкуснейший суп из курицы с лапшой).</p>

