        <h5>Санитарный контроль</h5>
        <p>Для граждан Казахстана не требуется предъявления каких-либо специальных медицинских документов, подтверждающих состояние здоровья, а также наличие тех или иных прививок.</p>

        <p>Основные меры карантинного характера в отношении здоровья пассажиров применяются службами пунктов пропуска еще до момента пересечения границы Японии на так называемых «санитарных кордонах», оборудованных техническими средствами наблюдения за состоянием прилетевших граждан, в том числе путем бесконтактного измерения температуры тела тепловизорами.</p>

        <h5>Фитосанитарный контроль</h5>

        <p>На некоторые растения еще до их ввоза в Японию в течение срока их вегетативного периода необходимо провести процедуру карантинной инспекции на территории Казахстана и получить соответствующий сертификат.</p>
        <p>К таким растениям относятся:</p>
        <ul>
            <li>живые клубни и подземная часть некоторых видов культивируемой сахарной свеклы;</li>
            <li>подземная часть рассады некоторых видов культивируемого томата;</li>
            <li>живые клубни и рассада некоторых видов культивируемого картофеля;</li>
            <li>семена гороха, предназначенные к посадке; </li>
            <li>семена фасоли, предназначенные к посадке.</li>
        </ul>

        <p>Растения при ввозе в Японию в обязательном порядке должны пройти фитосанитарный контроль до момента перемещения через границу.</p>

        <p>При этом пограничные власти в пункте пропуска требуют предоставить на перевозимые растения и их семена из тех или иных регионов соответствующие фитосанитарные сертификаты об отсутствии угрозы распространения определенных заболеваний.</p>

        <p>Для прохождения процедуры фитосанитарного контроля необходимо за семь дней до предполагаемой даты въезда в Японию оформить заявку на фитосанитарный контроль Application for Import Plant Inspection в Службе фитосанитарного контроля (Plant Protection Station) пункта пропуска через границу. </p>

        <p>Аналогичная процедура действует при вывозе с территории Японии тех или иных видов растений. </p>

        <p>Более подробная информация размещена на сайте Службы фитосанитарного контроля - <a href="http://www.pps.go.jp/english" target="_blank">pps.go.jp</a> (на английском языке).</p>

        <h5>Ветеринарный контроль</h5>

        <p>Любые ввозимые в Японию животные проходят карантинную инспекцию в аэропортах и морских портах. </p>

        <p>Некоторые из животных (например, из семейства грызунов) запрещены к ввозу в страну.</p>

        <p>Если ввоз животных разрешен, то на них в обязательном порядке должно быть оформлено ветеринарное свидетельство, выданное компетентными государственными органами стран-экспортёров, доказывающее факт отсутствия болезней.</p>

        <p>Согласно действующих в стране правил ввоза (вывоза) животных, на каждое животное требуется сертификат Approval of Import Inspection of Animals, полученный от Службы карантинного контроля животных (Animal Quarantine Service).</p>

        <p>Для получения данного сертификата необходимо за 40 дней до предполагаемого ввоза животного в страну направить в адрес AQS заявку (Notification of Import Inspection).</p>

        <p>Аналогичные процедуры необходимо осуществить и при вывозе домашнего животного с территории Японии.</p>

        <p>Более подробную информацию о правилах ветеринарного контроля при ввозе/вывозе домашних животных с территории Японии можно найти на сайте Службы карантинного контроля - <a href="http://www.maff.go.jp/aqs/english/index.html" target="_blank">maff.go.jp</a> (на английском языке).</p>

