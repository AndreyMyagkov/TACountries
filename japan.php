        <h2>Безопасность</h2>

        <p>Благодаря жёстким миграционным законам и высокому уровню правопорядка Япония является достаточно спокойной страной.</p>

        <p>В преддверии проведения Летних Олимпийских игр в 2020 г. службы безопасности и правоохранительные органы Токио и других крупных городов проводят регулярные учения по реагированию на возможные террористические угрозы, массовые беспорядки, усилен контроль на пропускных пунктах в международных аэропортах и морских портах.</p>

