        <h2>Таможенный контроль</h2>

        <p>В соответствии с японским законодательством грузы и личные вещи, принадлежащие иностранным физическим и юридическим лицам, проходят таможенный досмотр. </p>

        <p>Предусматривается система т.н. «зеленого» и «красного» коридоров.</p>

        <p>По «зеленому» коридору проходят пассажиры без багажа или имеющие вещи, пропускаемые без взимания таможенных пошлин.</p>

        <p>«Красным» коридором пользуются пассажиры, в багаже которых имеются предметы, подлежащие декларированию, или товары, ввоз которых в Японию ограничен или запрещен.</p>

        <p>О действующих в Японии таможенных правилах подробнее можно узнать на сайте Таможенной службы Японии: <a href="http://www.customs.go.jp/kaisei/youshiki/form_C/C5360-Er.pdf">customs.go.jp</a></p>
        <h5>Запрещен ввоз в Японию:</h5>
        <ul>
            <li>химических веществ и биологических материалов, которые могут быть использованы для производства оружия массового поражения;</li>
            <li>наркотиков всех видов и психотропных веществ, а также средств для их производства и употребления;</li>
            <li>фальшивых ценных бумаг и денежных знаков;</li>
            <li>книг, журналов, рисунков и гравюр, имеющих аморальное содержание, детской порнографии; </li>
            <li>печатных изданий, подрывающих общественный порядок (призывающих к насилию, массовым выступлениям и свержению государственной власти и т.д.);</li>
            <li>предметов, наносящих ущерб интеллектуальной собственности (торговой марке, авторскому и патентному праву) и поддельных товарных изделий.</li>
        </ul>
        <h5>Согласно действующих в Японии правил, в страну запрещен ввоз следующих семян и растений:</h5>
        <ul>
            <li>клубней некоторых видов картофеля; </li>
            <li>рассады риса, рисовой соломы, в том числе изделий из рисовой соломы (циновки, мешки, коврики и т.д.); </li>
            <li>плодов некоторых фруктов (абрикоса, черешни, вишни, сливы, персика, груши, айвы, грецкого ореха);</li>
            <li>соломы и листьев растений Hordeum, Triticum и Secale (в том числе соломы в виде упаковочных материалов и аналогичных товаров из нее), а также соломы и листьев растений рода пырей.</li>
        </ul>
