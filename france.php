                <p><strong>Un billet - </strong>одноразовый билет, действует в метро, трамвае, автобусе. Билеты на электричку RER нужно покупать отдельно. Такой билет дает возможность перемещаться в одну сторону. Цена 1.90 евро. Для большого количества поездок выгоднее купить <strong>La carnet</strong> - книжечку на 10 билетов, она обойдется всего в 14.5 евро.</p>
                <p>Льготные билеты предназначены для пенсионеров, детей старше 6 лет, студентов. Приобрести их можно в кассе метро, предъявив соответствующий документ. </p>
                <p>Весь Париж и его пригороды поделены на зоны, по которым идет расчет стоимости билетов. Так простой билет предназначен для пользования транспортом в пределах города. Если же необходимо съездить в Версаль или пора возвращаться в  аэропорт,  то нужно купить билет на RER, при покупке сказать станцию назначения. За каждую зону цена растет на 50 центов. Билет в аэропорт обойдется в 7.8 евро.</p>
