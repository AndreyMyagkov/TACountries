                <h5>Метро</h5>
                <p>Парижского метрополитена боятся многие туристы, но он достаточно прост если понять его принцип. Найдя на карте необходимую станцю, нужно определиться на какой линии она находится и посмотреть как называется конечная станция по ходу движения. Это все что потребуется, чтобы без проблем добраться до места. Все указатели в метро обозначают цвет линий и конечную станцию. В центре множество станций пересадок, все они обозначены соотвествующими указателями. Заблудиться будет очень сложно.</p>
                <p>Парижское метро состоит из 16 линий общей протяженностью 220 км, на которых находятся 383 станции, учитывая пересадочные</p>
                <p>Все линии имеют свой цвет и номер. Всего номеров 14 и две линии короткие, это ответвления от 7 и от 3 ветки. Проще всего отличать их по номеру, так как многие цвета отличаются лишь оттенком.</p>
                <p>Интервал движения 2 минуты, в вечернее время — 12 минут.</p>
                <p>Двери в вагонах не открываются автоматически. Для того чтобы войти в вагон или выйти из него нужно нажать на кнопку у двери на новых линиях или резко дернуть ручку на старых.</p>
                <p>Поезда движуться в среднем со скоростью 35 км/ч. Объявления станций произносят только на французском языке, следить за дорогой лучше по надписям на самих станциях или в новых вагонах есть электронное табло.</p>
                <p>На новой 14 линии метро организовано автоматическое движение поездов без машиниста. Направляясь на вокзал Gare de Lion можно прокатиться в первом вагоне в том месте, где обычно расположена кабина машиниста.</p>


