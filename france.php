                <p>Автобусы останавливаются на остановках по требованию, для этого нажмите на кнопку в салоне перед дверью, тогда на следующей остановке сможете выйти. Объявления остановок происходит с помощью бегущей строки, что значительно комфортнее воспринимается для туриста не знающего французский.</p>

