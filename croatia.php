                <h2>Культура и традиции</h2>
                <p>Хорватская культура формировалась постепенно из глубины веков и до наших дней. Города здесь хранят все архитектурные стили, начиная с античности. К примеру, желающим заглянуть в древний Рим непременно стоит посетить Пулу и пройтись по отлично сохранившемуся амфитеатру. После этого можно заглянуть в Сплит в гости к Диоклетиану. Конец первого тысячелетия запечатлен в базиликах Пореча, Задара и Трогира. Начало средних веков принесло в Хорватию романский стиль с характерными для него храмами, которые сегодня можно увидеть в Загребе, Трогире и на острове Крк. Средневековье знаменуется османской угрозой, а вместе с ней возникновением каменных крепостей по всему побережью. С традициями эпохи возрождения можно познакомиться в Дубровнике, на Хваре и на Корчуле, а полюбоваться любимой Европой готикой — в сердце Загреба.</p>
                <p>Хорватия интересна не только архитектурой — здесь богатая коллекция скульптур местных мастеров, которые представлены на улицах Сплита, Загреба и других городов, а также в храмах Трогира и Шибеника.</p>
                <p>Хорватия бережно относится к своим традициям — в городах сохранились народные промыслы, такие как плетение кружев или создание деревянных игрушек. На местные праздники хорваты одеваются в традиционные одежды, а все народные гуляния непременно сопровождаются национальными песнями и танцами. Кстати, в дни национальных праздников в Хорватии не принято выходить на работу, даже если это продуктовый магазин или ресторан, поэтому хорваты запасаются заранее достаточным количеством еды и местного вина, чтобы отметить событие.</p>
                <p>К традициям Хорватии можно отнести и ее кухню. Она впитала в себя многие иностранные влияния, которые испытала на себе за всю историю. В основе хорватской кухни — простые, но свежие продукты, выращенные в собственном саду или выловленные в море. Хорваты на побережье любят и умеют готовить рыбу, а в остальных регионах страны более популярны мясные блюда. Туристам непременно стоит попробовать: пршут — копченый свиной окорок, буреки — мясные или сырные пироги из слоеного теста, бродет — несколько сортов рыбы, которые подаются с отварным рисом. В центре страны можно отведать вкусную паштицаду — запеченую говядину или индейку с кукурузным тестом. Порции, как правило, огромные, а радушные владельцы «конобе» (национальная таверна) любят угощать гостей, особенно постоянных, домашней настойкой.</p>

