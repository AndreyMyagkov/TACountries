                <h2>Культура и традиции</h2>
                <p>На культуру Монголии, ее самобытность и аутентичность, отпечаток накладывало соседство с Китаем и Тибетом, а также тот факт, что коренное население издревле вело кочевой образ жизни.</p>
                <p>Жители Монголии — крайне дружелюбный и гостеприимный народ, придерживающиеся законов степи, которые гласят помогать всем странникам. Любого гостя сразу же приглашают в дом, выставляют на стол все лучшие угощения и ведут неторопливую беседу.</p>
                <p>Подавляющее большинство местного населения, в том числе и в столице, проживают в традиционных юртах — переносных конусообразных жилищах из войлока. С ними связано множество обычаев и традиций, которые могут запутать неопытного туриста, поэтому рекомендуется наблюдать за действиями хозяина и поступать аналогичным образом. Так, например, порог в доме считается символом спокойствия и благосостояния семьи, поэтому разговаривать через него не следует, также не принято наступать на порог и садиться на него.</p>
