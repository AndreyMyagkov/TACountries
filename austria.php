                <h5>Метро</h5>
                <p>Венский метрополитен (U-Bahn) открылся 8 мая 1976 года, однако его сегодняшняя сеть включает в себя также ряд участков S-Bahn, построенных в 1898–1901 годах. Венский метрополитен обслуживается фирмой Wiener Linien GmbH & Co KG, принадлежащей компании Wiener Stadtwerke AG, 100% акций которой находится во владении муниципалитета города Вена.
                </p>
                <p> Венское метро состоит из 5 линий общей протяженностью 68,9 км.
                </p>
                <p>Линии пронумерованы от U1 до U6, при этом строительные проекты, связанные с линий U5, так и не были реализованы. Венские линии метро имеют цветовые обозначения: U1 — красная; U2 — фиолетовая; U3 — оранжевая; U4 — зеленая; U6 — коричневая. Все линии U-Bahn, кроме U6, связывают различные районы города с его центром.</p>
                <p>Поезда U-Bahn курсируют с интервалом 5 минут, в вечернее время — 7 минут.</p>
                <p>Двери в вагонах U-Bahn не открываются автоматически. Для того чтобы войти в вагон или выйти из него нужно нажать на кнопку у двери или резко дернуть дверную рукоятку.</p>
                <p>Для проезда в Венском метро в пределах города требуется обычный внутригородской билет, действующий на всех видах общественного транспорта.</p>
