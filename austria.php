                <p><strong>Streifenkarte  - </strong>Билет Einzelfahrschein в виде карточки, на которой нанесены 4 полосы — для четырех поездок, стоит 8,80 евро. Каждая полоса используется как билет на одну поездку. После того, как очередная полоска прокомпостирована, ее следует отогнуть так, чтобы сверху вновь оказалась чистая полоса. </p>
                <p><strong> Fahrschein Halbpreis  - </strong>льготный билет за половину цены. Он предназначен для детей 6–15 лет и пенсионеров, а также для перевозки в транспорте собак и велосипедов. Отметим, что дети до 6 лет освобождены от оплаты за проезд. Дети до 15 лет могут ездить бесплатно по воскресеньям, в дни больших праздников и во время школьных каникул. Льготный билет за полцены действителен для проезда в метро и в электропоездах до 2-х остановок, в трамвае и автобусе — до 3-х остановок. Чтобы узнать, в пределах каких зон действителен билет Fahrschein Halbpreis, нужно обратить внимание на схемы движения транспорта, размещенные на остановках и в салонах. Эти схемы состоят из отрезков пути, включающих несколько остановок. На этих отрезках, собственно, билет и действителен. Во всем остальном он полностью идентичен билету Einzelfahrschein.</p>
