        <p>Показатели преступности в Австрии – одни из наиболее низких среди развитых стран. А согласно исследованиям компании Mercer, Вена входит в 10 самых безопасных городов мира.</p>
        <p>За последние годы количество умышленных убийств на 100 тысяч жителей составило в Австрии всего около 0,6 в год (для сравнения, в России этот показатель чуть более 10).</p>
