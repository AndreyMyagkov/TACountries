                </p>
                <p>Первые автобусы с бензиновыми двигателями появились в Вене 23 марта 1907 года. Уже в 1913 году по венским улицам проехал первый двухэтажный автобус. С 60-х годов прошлого столетия в качестве топлива автобусы используют жидкий газ.</p>
