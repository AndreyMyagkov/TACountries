<section id="transport" class="bg-white">
    <div class="container">
        <h2>Транспорт</h2>
        <div class="row justify-content-between">
            <div class="col-md-8 col-lg-7">
                <p>В Боливии развито воздушное сообщение. Это самый быстрый и комфортный способ путешествовать в условиях высоких гор. В каждом более или менее крупном городе есть свой аэропорт. Самолеты надежны, летают часто, местных авиакомпаний 5 штук. Но этот вид транспорта не дешевый, плюс существенные пошлины и сборы еще больше повышают цену. Так, при любом перелете в цену включается сбор в 15000 боливиано, а при вылете из страны еще сверху надо добавить 25 долларов с человека. Путешествовать по стране можно и на автобусах, стоит только заранее убедиться, какой транспорт у перевозчика. В Боливии много фирм занимается автобусными перевозками по стране, и далеко не у всех имеются новые комфортные автобусы с кондиционером, есть и совершенно убитые машины, дорога в которых покажется сплошным мучением. Поезда в стране есть, но используются редко. Любимый маршрут туристов расположен в регионе солончака Уюни, он поражает воображение своими видами, а также дорогой по горному озеру.</p>

                <h5>Городской транспорт</h5>
                <p>В городах Боливии из общественного транспорта представлены автобусы и маршрутки. Разобраться в пути следования и расписании не всегда могут даже местные жители, туристам такое совсем не под силу. Этот вариант передвижения очень дешевый, но очень неудобный. Автобусы ходят часто, но разобраться куда они идут, будет сложно. Маршрутки отличаются только размером, во всем другом похожи на автобусы, идут тем же маршрутом, стоят почти столько же, ходят часто, но точного расписания или маршрута нет.</p>

                <h5>Фуникулер</h5>
