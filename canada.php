                <p>На воздушные перевозки приходится около 10% от всего транспортного потока в Канаде.<br />Крупнейшей авиакомпанией страны является Air Canada &mdash; ее воздушный флот составляют 368 самолетов.<br />На втором месте находится компания по перелетам CHC Helicopter, в собственности которой 150 вертолетов.<br />Третье место занимает WestJet &mdash; это бюджетная авиакомпания, владеющая 86 воздушными судами.</p>
                <p>Главными аэропортами страны являются: Международный аэропорт Торонто им. Пирсона, Международный аэропорт Ванкувера, Международный аэропорт Калгари и Международный аэропорт им. Пьера Эллиота Трюдо в Монреале.</p>

                <h5>Железная дорога</h5>
                <p>Железнодорожная система в стране отлично развита, ж/д сеть, а это 72 212 км путей, связывает все ключевые точки на территории государства.<br />Перевозки обеспечивают две крупнейшие компании: Canadian Pacific Railway и National Railway. <br />Передвижение на поезде является более затратным, чем на автобусе, но многие жители и гости страны считают этот способ перемещения на дальние расстояния самым комфортным. Тем более, что можно неплохо сэкономить, приобретая билеты в оба конца и/или заранее.</p>

                
                <h3>Городской транспорт</h3>

                <h5>Метро</h5>
                <p>Метрополитен (подземное метро и скоростные трамваи) представлен в 6 городах Канады: Оттава, Ванкувер, Торонто, Монреаль, Калгари и Эдмонд.<br />На каждой станции размещены маршрутные карты, также в кассе можно взять печатную копию. Наибольшее количество рейсов приходится на середину дня и часы пик.</p>

                <h5>Автобусное сообщение</h5>
                <p>Автобусные маршруты, как правило, начинают функционировать с 05:00 утра и до 1:30 ночи. Существуют также ночные рейсы &mdash; Blue Night Network.<br />Все автобусы оборудованы световым табло, на котором указываются номер маршрута и основные пункты назначения.</p>
                <p>Тарифы в городах несколько отличаются друг от друга, но все они имеют систему скидок при покупке проездных билетов: так, единый билет в Монреале обойдется в 3 CAD, дневной проездной стоит 12 CAD, на неделю &mdash; 43 CAD.</p>

                <h5>Такси</h5>
                <p>Такси в Канаде нельзя отнести к самому экономичному виду транспорта, но им выгодно пользоваться для поездок на короткие расстояния.<br />Стоимость проезда рассчитывается по счетчику, с учетом посадки и времени ожидания. В Канаде положено оставлять чаевые водителям &mdash; 10-15% от общей суммы оплаты.</p>

