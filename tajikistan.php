                <h2>Культура и традиции</h2>

                <p>Народ Таджикистана незыблемо чтит свои национальные обычаи и традиции, поэтому последующим поколениям они передаются практически в первоначальном, самобытном виде.</p>
                <p>Традиции соблюдаются в быту, особенностях проведения праздников и обрядов, отражаются в национальных блюдах и одежде.</p>
                <p>По сей день таджики, особенно в сельской местности, предпочитают носить национальную одежду: для женщин это длинное цветное платье с вышивкой и шаровары, голову обязательно покрывают платком, а мужчины надевают свободные штаны и рубаху, халат, тюбетейку и кожаные сапоги.</p>
                <p>У многих таджиков в селах внутреннее убранство дома выглядит почти так же, как и сотни лет назад: пол застилается коврами и паласами, по периметру комнаты разложены стеганые одеяла, а по центру — скатерть для приема пищи. Стены также украшены коврами, а вещи хранятся в кованых сундуках.</p>
                <p>Основными направлениями искусства в стране были и остаются ковроткачество, вышивание золотом и изготовление игрушек-оберегов, например, кукол Лухтак. Считается, что такие куклы оберегают дом и семью от злых духов.</p>
                <p>Одним из главных национальных праздников в стране является Навруз, что в переводе означает «Новый день», он отмечается в Таджикистане с 21 по 24 марта. В эти дни обязательно устраиваются народные гуляния с традиционными песнями и плясками, конные скачки и различные состязания.</p>
                <p>Неотъемлемым праздничным атрибутом является суманак — блюдо из проросшей пшеницы с добавлением муки и воды. Суманак раздают всем соседям, родственникам и близким, а те, в свою очередь, загадывают желание, которое обязательно исполнится в течение года.</p>
                <p>Другими традиционными блюдами Таджикистана являются:</p>
                <ul>
                    <li>курутоб — кушанье из лепешки, творога, овощей, зелени и льняного масла;</li>
                    <li>плов из риса (или булгура) с мясом, морковью, луком и приправами (зира, барбарис, чеснок, изюм и др.)</li>
                    <li>манты из рубленой баранины или говядины, подаются со сметаной и кислым молоком;</li>
                    <li>самбуса — сочные мясные пирожки;</li>
                    <li>шурбо — суп из баранины с добавлением овощей.</li>
                </ul>
                <p>Любая трапеза в Таджикистане венчается чаепитием с национальной выпечкой, орехами и сухофруктами.</p>
