                <p>Все культурные сферы мальтийцев, от языка и религии до архитектуры и национальной кухни, сложились под прямым влиянием множества народов, приезжающих на острова в качестве завоевателей и торговцев. К ним относятся римляне, арабы, финикийцы, византийцы, англичане и французы.</p>
