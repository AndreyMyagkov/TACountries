                <p>Основными направлениями искусства в стране были и остаются живопись, плетение воздушного кружева, изготовление ювелирных украшений и выдувание замысловатой стеклянной посуды. Существуют даже целые ремесленные деревни, специализирующиеся на каком-либо промысле, например, деревни Та-Али и Та‘ Дбиги, привлекающие множество туристов.</p>
