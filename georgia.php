                <p>Грузинская Культура — древняя, таинственная и экзотическая, удивительным образом сочетающая в себе элементы персидской, османской, арабской, анатолийской, дальневосточной и европейской цивилизаций.</p>
                <p>Национальными качествами местного народа традиционно считаются искренность, радушие, непосредственность и чувство собственного достоинства. Грузинская легенда гласит, что именно эти черты характера понравились Господу настолько, что он подарил грузинам самую лучшую землю. Одной из основополагающих культурных традиций Грузии является гостеприимство. Гостей в стране уважают, чтут и любят. Есть даже местная пословица: «гостей посылает сам Создатель». Туристы после посещения Грузии с теплотой вспоминают, как радушные хозяева с огромным почтением и вниманием относятся к приезжим и стараются во всем угодить.</p>
                <p>Главной ценностью грузинского народа и основой общества всегда являлась и является семья, где традиционно мужчине отводится роль защитника и добытчика, а женщине — роль матери, хозяйки, хранительницы очага.</p>
                <p>В обществе с большим уважением относятся к старшим, а в детях стараются воспитывать такие качества, как честность, открытость, взаимопомощь.</p>
                <p>Ярким проявлением местной культуры являются также музыка, одиночные, парные и групповые танцы («Картули», «Хоруми», «Ачарули») и знаменитое хоровое песнопение, исполняемое в особой технике пения на три голоса.</p>
                <p>Говоря о традициях, нельзя не упомянуть и самобытную национальную кухню Грузии. Ее отличает разнообразие мясных (баранина, телятина, птица) и овощных блюд, щедро дополняемых медом, вином, зеленью и ароматным специями.</p>
                <p>Любой ресторан предлагает отведать гостям страны вкуснейшие сациви, суп-харчо, хачапури, хинкали, мивади, чахохбили, лобио, кутабы, долму, сулугуни, копченые колбаски, и, разумеется, лучшие соусы: жгучую аджику, сладкий с кислинкой ткемали и ореховый сацебели. В качестве хлеба популярны «мчади» — вкуснейшие кукурузные лепешки.</p>
                <p>Символом Грузии, несомненно, является вино. Считается, что именно здесь зародилось виноделие еще 8 тысячелетий назад. На сегодняшний день в стране культивируется 500 видов винограда, благодаря чему сортов вин в Грузии насчитывается великое множество. Самые знаменитые из них: «Саперави», «Киндзмараули», «Хванчкара», «Мукузани», «Телиани», «Ахашени», «Тбилисури», «Цинандали», «Кахети». Из крепких напитков производят виноградную водку «чачу», а из некрепких — множество сортов грузинского пива.</p>
                <p>Грузинская культура лучше всего познается гостями страны во время «супры» — застолья под руководством тамады с огромным ассортиментом национальных блюд и вина, под длительные и поучительные тосты по любому поводу. Супра, как правило, сопровождается хоровым пением и исполнением национальных танцев.</p>


