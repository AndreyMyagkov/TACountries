        <h5>Спиртное и сигареты</h5>
        <p>На территорию Азербайджана разрешается ввозить алкогольную продукцию в объеме, не превышающем 3 литра, сигарет в количестве не более 600 шт.</p>

        <h5>Ювелирные украшения и драгоценности</h5>
        <p>Для ввоза/вывоза ювелирных изделий необходимо наличие сертификата.</p>

        <h5>Художественные и культурно-исторические ценности</h5>
        <p>Ввоз художественных, исторических, научных и иных предметов культуры на территорию Азербайджана не ограничен, уплата таможенных платежей не требуется. Культурные ценности, включенные Министерством культуры и туризма в охранный перечень, могут вывозиться с территории страны после представления в таможенные органы свидетельства о возможности их экспорта, выданного Министерством культуры и туризма Азербайджана, и уплаты таможенной пошлины в размере 100% от их комиссионной стоимости.</p>

        <h5>Оружие и боеприпасы</h5>
        <p>Правила, регулирующие ввоз/вывоз оружия и боеприпасов, аналогичны казахстанским.</p>

        <h5>Автомобили</h5>
        <p>При осуществлении таможенного оформления ввозимого автомобиля на срок более 30 суток, на депозитный счет таможенного органа Азербайджана вносится сумма, равная объему таможенных платежей для выпуска автомобиля в свободное обращение, и оформляется обязательство об обратном вывозе упомянутого транспортного средства. Аналогично платежи взимаются, если автомобиль временно ввозится на территорию страны на срок до 30 дней не самим владельцем, а другими лицами по доверенности. Срок следования транзитом через территорию Азербайджана ограничен 3 сутками.</p>
        <p>С 16 августа 2014 года в Азербайджане ужесточены требования к временному ввозу транспортных средств, не соответствующих экологическому стандарту Евро-4. С владельцев транспортных средств, временно ввозимых на территорию страны, могут быть потребованы подтверждающие документы.</p>

        <h5>Ковры</h5>
        <p>В Азербайджане существует государственная монополия на экспорт изготовленных в стране ковров и ковровых изделий. Для вывоза приобретенной здесь ковровой продукции необходимо специальное разрешение Министерства культуры Азербайджана.</p>

