                <h5>Городской транспорт</h5>
                <p>Автобусы - самый дешевый и в принципе единственный вид городского транспорта, не считая такси во многих городах страны. В Лиме движение организовано с помощью разного вида автобусов. Синие ходят по центральным маршрутам, у них есть схема движения и расписание, двигаются они по выделенной полосе, поэтому пробки столицы им не грозят, проезд стоит от 1.2 соль. Из спальных районов к крупным магистралям идут небольшие местные автобусы за 0.5-1.5 соль в зависимости от расстояния. Дублируют крупные автобусы мелкие минибусы или маршрутки, стоят чуть подороже, но ходят чаще и их не нужно ждать. Для быстрого и комфортного перемещения между основными районами города удобно пользоваться метробусом. Всего по городу проложено 5 линий, автобусы комфортные, непереполненные, ходят регулярно.</p>

                <h5>Такси</h5>
